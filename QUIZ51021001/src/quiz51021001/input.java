    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double tugas=Double.parseDouble (jTextField3.getText()); 
        double NilaiTugas= tugas*0.2; 
        double quiz=Double.parseDouble (jTextField4.getText()); 
        double NilaiQuiz= quiz*0.2;
        double mid=Double.parseDouble (jTextField5.getText()); 
        double NilaiMid= mid*0.3; 
        double uas=Double.parseDouble (jTextField6.getText()); 
        double NilaiUAS= uas*0.3; 
        double total=NilaiTugas+NilaiQuiz+NilaiMid+NilaiUAS;
        jTextField7.setText(String.valueOf(total));
                
        double tot=Double.parseDouble(jTextField7.getText());
        if (tot >= 80){
            jTextField8.setText("A");    
        } 
        else if (tot>=70 && tot<80){
            jTextField8.setText("B");
        }
        else if (tot>=60 && tot<70){
            jTextField8.setText("C");
        }
        else if (tot>=50 && tot<60){
            jTextField8.setText("D");
        }
        else {
            jTextField8.setText("E");
        }
        // Panggil metode simpanData() setelah perhitungan nilai
        simpandata();
        
    
    }//GEN-LAST:event_jButton1ActionPerformed

