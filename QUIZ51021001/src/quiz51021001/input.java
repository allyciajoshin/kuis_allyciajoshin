    private void simpandata(){
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("quiz51021001.dat", true))) {
            String nim = jTextField1.getText();
            String nama = jTextField2.getText();
            String nilaiTugas = jTextField3.getText();
            String nilaiQuiz = jTextField4.getText();
            String nilaiMid = jTextField5.getText();
            String nilaiFinal = jTextField6.getText();
            String total = jTextField7.getText();
            String huruf =jTextField8.getText();

            // Format data yang akan disimpan ke dalam file
            String dataMahasiswa = nim + "," + nama + "," + nilaiTugas + "," + nilaiQuiz + "," + nilaiMid + "," + nilaiFinal + "," + total+","+ huruf+ "\n";
            writer.write(dataMahasiswa);
        } catch (IOException e) {
            e.printStackTrace();
        }
        
    }
